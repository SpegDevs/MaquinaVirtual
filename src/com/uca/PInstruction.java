package com.uca;

public class PInstruction {

    private PCode pcode;
    private int ni;
    private int di;

    public PCode getPcode() {
        return pcode;
    }

    public void setPcode(PCode pcode) {
        this.pcode = pcode;
    }

    public int getNi() {
        return ni;
    }

    public void setNi(int ni) {
        this.ni = ni;
    }

    public int getDi() {
        return di;
    }

    public void setDi(int di) {
        this.di = di;
    }
}
